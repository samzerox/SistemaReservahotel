    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:

        if (!txtidconsumo.getText().equals("")) {
            int confirmacion=JOptionPane.showConfirmDialog(rootPane, "Esta Seguro Que Desea Eliminar El Producto?","Confirmar",2);

            if (confirmacion==0) {
                fconsumo func=new fconsumo();
                vconsumo dts= new vconsumo();

                dts.setIdconsumo(Integer.parseInt(txtidconsumo.getText()));
                func.eliminar(dts);
                mostrar(idreserva);
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

